//___пишем в / /
/b./; //- b и любой другой символ (. - точка любой другой символ)
/[a-z]/; // любой из них
/$/; // конец строки
/^/; //начало строки
/\./; // экранирование
/^[^b]/; //начинаеться не с b
/\d\d\d/; // любая цифра \d (3 любых цифры)
/\D/; //все что угодно кроме цифр
/\s/; //ищет пробелы
/en\s/; // ищем слова заканчивающейся на en
/\S/; // все кроме пробелов
/\w/; // любая буква
/\W/; // все кроме букв
/\b/; // граница слова
/\B/; // не граница слова

//квантификация - не дублировать регулярные выражения
/n{3}/; //искать n  подряд 4 раза
/\b\w{3}\b/; // {3} - чтобы не писать повторы
/n{4,6}/; //искать n от 4 до 6
/be*/; //от нуля и выше (*)
/be+/; //от 1 и выше (+)
/be?/; // 1 или 0 (?)

