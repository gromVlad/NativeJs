//регулярные выражений
//позволяет работать с текстом (отдельный язык програмирование , обычно на всех языках схож синтаксис)
//Вот пример кода на JavaScript, который использует регулярное выражение для проверки корректности email-адреса:
function validateEmail(email) {
  const regex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  return regex.test(email);
}

// Пример использования
